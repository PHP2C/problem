<?php
/**
 * 这里是为了练习 and 运算符 &
 * & 按位与
 * 将 $a 和 $b 中都为 1 的位设置为 1
 */
$a = 2; // 0000 0010
$b = 3; // 0000 0011
echo $a & $b; //0000 0010

$format = '(%1$d = %1$04b) = (%2$2d = %2$04b)'
. ' %3$s (%4$2d = %4$04b)' . "\n";

printf($format,$a,$b,'&',$a&$b);

var_dump([$a,$b]);
$a = 1;
$b = 3;
// 交换值
$a = $a ^ $b;
$b = $a ^ $b;
$a = $b ^ $a;
var_dump([$a,$b]);
